bool sieve[mxn];
vector<int> prime;
void linear_sieve(){
  for(int i=2;i<mxn;++i){
  	if(!sieve[i]) prime.pb(i);
  	for(int p:prime){
  		if(i*p>=mxn) break;
  		sieve[i*p]=1;
  		if(i%p==0) break;
  	}
  }
}
